package Basics;

public class Strings {
    public static void main(String[] args) {
        // Strings
        int aValue = 5;
        double aDouble = 2.6;
        String text = "Hello Everyone!";
        String blank = " ";
        String moreText = "I hope you all are fine.";
        System.out.println(text);
        System.out.println(text + blank + moreText);
        System.out.println("My number: " + aValue + ", " + aDouble);
    }
}
